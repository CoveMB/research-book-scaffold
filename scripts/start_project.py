def apply_change(
    change: PlannedChange,
    options: StartProjectOptions,
    result: StartProjectResult,
) -> None:
    result.planned_files.append(change.path)
    if change.path.exists():
        existing_text = read_text(change.path)
        if existing_text == change.content:
            result.unchanged_files.append(change.path)
            return
        if not is_safe_to_replace(change.path, existing_text):
            if not options.force:
                warning = skipped_change_warning(change.path, existing_text)
                result.warnings.append(warning)
                result.skipped_files.append(change.path)
                if options.dry_run:
                    result.dry_run_diffs.append(render_diff(change.path, existing_text, change.content))
                return
            result.protected_force_files.append(change.path)
            if options.dry_run:
                result.warnings.append(f"Dry-run: --force would replace protected content: {change.path}")
            else:
                result.warnings.append(f"Replacing protected content because --force was supplied: {change.path}")
            if options.dry_run:
                result.dry_run_diffs.append(render_diff(change.path, existing_text, change.content))
                return
        if options.dry_run:
            result.dry_run_diffs.append(render_diff(change.path, existing_text, change.content))
            return
    elif options.dry_run:
        result.dry_run_diffs.append(render_diff(change.path, "", change.content))
        return

    write_text_file(change.path, change.content)
    result.changed_files.append(change.path)


def is_safe_to_replace(path: Path, existing_text: str) -> bool:
    relative_path = relative_to_projectish(path)
    markers = SCAFFOLD_MARKERS_BY_PATH.get(relative_path, ())
    return any(marker in existing_text for marker in markers)


def skipped_change_warning(path: Path, existing_text: str) -> str:
    if GENERATED_MARKER in existing_text:
        return (
            f"Skipped {path}: generated file differs from the planned initializer output; "
            "rerun with --force only if those local edits should be replaced."
        )
    return f"Skipped {path}: non-scaffold content is present; rerun with --force to replace it."


def relative_to_projectish(path: Path) -> Path:
    parts = path.parts
    for marker in ("manuscript", "notes", "research", "bibliography", "templates"):
        if marker in parts:
            return Path(*parts[parts.index(marker) :])
    return Path(path.name)


def render_diff(path: Path, old_text: str, new_text: str) -> str:
    old_lines = old_text.splitlines(keepends=True)
    new_lines = new_text.splitlines(keepends=True)
    return "".join(
        difflib.unified_diff(
            old_lines,
            new_lines,
            fromfile=f"{path} (current)",
            tofile=f"{path} (planned)",
        )
    )


def project_relative_path(
    project_root: Path,
    raw_path: str,
    fallback: Path,
    result: StartProjectResult,
) -> str:
    project_root_resolved = project_root.resolve()
    requested_path = Path(raw_path).expanduser()
    candidate = requested_path if requested_path.is_absolute() else project_root / requested_path
    resolved = candidate.resolve()
    try:
        return resolved.relative_to(project_root_resolved).as_posix()
    except ValueError:
        result.warnings.append(
            f"Bibliography path {raw_path!r} is outside the project root; using {fallback.as_posix()}."
        )
        return fallback.as_posix()


def initialize_project(
    project_root: Path,
    answers: Mapping[str, object],
    options: StartProjectOptions,
    *,
    runner_func: CommandRunner | None = None,
    which_func: WhichFunc = shutil.which,
) -> StartProjectResult:
    normalized_answers = normalize_answers(answers)
    result = StartProjectResult()
    normalized_answers["bibliography_path"] = project_relative_path(
        project_root,
        string_value(normalized_answers["bibliography_path"]),
        DEFAULT_BIBLIOGRAPHY_PATH,
        result,
    )
    for warning in answer_warnings(normalized_answers):
        result.warnings.append(warning)
    for change in build_planned_changes(project_root, normalized_answers, result):
        apply_change(change, options, result)

    run_post_initialization(project_root, normalized_answers, options, result, runner_func, which_func)
    result.next_steps.extend(manual_next_steps(project_root, normalized_answers, options, which_func))
    result.exit_code = 1 if result.failed else 0
    return result


def answer_warnings(answers: Mapping[str, object]) -> list[str]:
    warnings: list[str] = []
    unknown_formats = [item for item in ensure_string_list(answers["output_formats"]) if item not in KNOWN_QUARTO_FORMATS]
    if unknown_formats:
        warnings.append(
            "Unsupported Quarto output format(s) recorded but not added to manuscript/_quarto.yml: "
            + ", ".join(unknown_formats)
        )
    better_bibtex_status = string_value(answers["better_bibtex_auto_export"]).lower()
    if better_bibtex_status == "no":
        warnings.append("Better BibTeX auto-export is not configured; set it up before citing Zotero sources.")
    elif better_bibtex_status != "yes":
        warnings.append("Better BibTeX auto-export is unresolved; verify Zotero export before citing sources.")
    return warnings


def manual_next_steps(
    project_root: Path,
    answers: Mapping[str, object],
    options: StartProjectOptions,
    which_func: WhichFunc,
) -> list[str]:
    steps: list[str] = []
    bibliography_path = string_value(answers["bibliography_path"])
    bibliography_file = project_root / bibliography_path
    better_bibtex_status = string_value(answers["better_bibtex_auto_export"]).lower()
    if better_bibtex_status != "yes":
        steps.append(f"Set up Zotero + Better BibTeX auto-export to `{bibliography_path}` before citing sources.")
    if not bibliography_has_verified_entries(bibliography_file):
        steps.append(f"Export verified references into `{bibliography_path}` before adding manuscript citations.")
    if is_unresolved(answers["citation_style"]):
        steps.append("Choose a citation style before release or publisher-specific export.")
    if is_unresolved(answers["target_venue"]):
        steps.append("Record the target venue or publisher once that decision is known.")
    if should_run_step(answers["use_obsidian"]):
        steps.append("Open this repository as the Obsidian vault before relying on Obsidian-only links or views.")
    if should_run_step(answers["strict_placeholder_detection"]):
        steps.append("Resolve release-check-visible placeholders before sharing or exporting the manuscript.")
    if should_run_step(answers["render_after_initialization"]) and not options.skip_render and not which_func("quarto"):
        steps.append("Install Quarto before rendering the manuscript.")
    if not steps:
        steps.append("Review the generated files, then run `make audit`.")
    return steps


def bibliography_has_verified_entries(path: Path) -> bool:
    if not path.exists():
        return False
    for line in read_text(path).splitlines():
        stripped = line.strip()
        if stripped and not stripped.startswith("%"):
            return True
    return False


def run_post_initialization(
    project_root: Path,
    answers: Mapping[str, object],
    options: StartProjectOptions,
    result: StartProjectResult,
    runner_func: CommandRunner | None,
    which_func: WhichFunc,
) -> None:
    runner = runner_func or default_command_runner
    if should_run_step(answers["run_audit_after_initialization"]) and not options.skip_audit:
        run_command(["bash", "setup.sh", "--dry-run"], project_root, "setup dry-run", options, result, runner)
        run_command(
            ["python3", "scripts/research-writing/check_manuscript_readiness.py"],
            project_root,
            "manuscript readiness check",
            options,
            result,
            runner,
        )
        run_command(["make", "audit"], project_root, "repository audit", options, result, runner)
    elif options.skip_audit:
        result.commands_skipped.append("post-initialization audit skipped by --skip-audit")

    if should_run_step(answers["render_after_initialization"]) and not options.skip_render:
        run_optional_render(project_root, answers, options, result, runner, which_func)
    elif options.skip_render:
        result.commands_skipped.append("manuscript render skipped by --skip-render")


def default_command_runner(command: Sequence[str], cwd: Path) -> subprocess.CompletedProcess[str]:
    return subprocess.run(list(command), cwd=cwd, check=False, text=True)


def run_command(
    command: Sequence[str],
    project_root: Path,
    label: str,
    options: StartProjectOptions,
    result: StartProjectResult,
    runner: CommandRunner,
) -> None:
    printable = " ".join(command)
    if options.dry_run:
        result.commands_skipped.append(f"dry-run would run {printable}")
        return
    try:
        command_result = runner(command, project_root)
    except OSError as error:
        result.failed.append(f"{label} failed to start: {error}")
        return
    result.commands_run.append(printable)
    if command_result.returncode != 0:
        result.failed.append(f"{label} exited {command_result.returncode}: {printable}")


def run_optional_render(
    project_root: Path,
    answers: Mapping[str, object],
    options: StartProjectOptions,
    result: StartProjectResult,
    runner: CommandRunner,
    which_func: WhichFunc,
) -> None:
    if not which_func("quarto"):
        result.warnings.append("Quarto is not available; skipped optional manuscript render.")
        return
    output_format = first_supported_render_format(answers)
    command = ["bash", "scripts/research-writing/render.sh"]
    if output_format:
        command.extend(["--to", output_format])
    if options.dry_run:
        result.commands_skipped.append(f"dry-run would run {' '.join(command)}")
        return
    try:
        render_result = runner(command, project_root)
    except OSError as error:
        result.warnings.append(f"Optional render failed to start: {error}")
        return
    result.commands_run.append(" ".join(command))
    if render_result.returncode != 0:
        result.warnings.append(f"Optional render exited {render_result.returncode}; inspect render output before export.")


def first_supported_render_format(answers: Mapping[str, object]) -> str | None:
    for output_format in ensure_string_list(answers["output_formats"]):
        if output_format in KNOWN_QUARTO_FORMATS:
            return output_format
    return None


def should_run_step(value: object) -> bool:
    return string_value(value).strip().lower() in {"y", "yes", "true", "1"}


def print_preflight_summary(project_root: Path, answers: Mapping[str, object], which_func: WhichFunc) -> None:
    print_text_section("Preflight summary", preflight_summary_lines(project_root, answers, which_func))


def preflight_summary_lines(project_root: Path, answers: Mapping[str, object], which_func: WhichFunc) -> list[str]:
    bibliography_path = string_value(answers.get("bibliography_path")) or DEFAULT_BIBLIOGRAPHY_PATH.as_posix()
    bibliography_file, bibliography_display = preflight_bibliography_path(project_root, bibliography_path)
    target_paths = [
        PROJECT_START_PATH,
        QUARTO_CONFIG_PATH,
        MANUSCRIPT_INDEX_PATH,
        PROJECT_CHARTER_PATH,
    ]
    existing_targets = [path.as_posix() for path in target_paths if (project_root / path).exists()]
    return [
        f"Existing {PROJECT_START_PATH}: {found_text(project_root / PROJECT_START_PATH)}",
        f"Bibliography path: {bibliography_display} ({bibliography_status(bibliography_file)})",
        f"Quarto: {tool_status('quarto', which_func)}",
        f"Pandoc: {tool_status('pandoc', which_func)}",
        f"Audit target: {make_target_status(project_root, 'audit')}",
        "Initializer targets already present: " + (", ".join(existing_targets) if existing_targets else "none"),
    ]


def preflight_bibliography_path(project_root: Path, raw_path: str) -> tuple[Path, str]:
    project_root_resolved = project_root.resolve()
    requested_path = Path(raw_path).expanduser()
    candidate = requested_path if requested_path.is_absolute() else project_root / requested_path
    try:
        relative_path = candidate.resolve().relative_to(project_root_resolved)
    except ValueError:
        fallback = project_root / DEFAULT_BIBLIOGRAPHY_PATH
        return fallback, f"{raw_path} is outside project root; will fall back to {DEFAULT_BIBLIOGRAPHY_PATH.as_posix()}"
    return project_root / relative_path, relative_path.as_posix()


def found_text(path: Path) -> str:
    return "found" if path.exists() else "not found"


def bibliography_status(path: Path) -> str:
    if not path.exists():
        return "missing"
    return "has verified-looking entries" if bibliography_has_verified_entries(path) else "empty or comments only"


def tool_status(tool_name: str, which_func: WhichFunc) -> str:
    return "available" if which_func(tool_name) else "not found"


def make_target_status(project_root: Path, target_name: str) -> str:
    makefile = project_root / "Makefile"
    if not makefile.exists():
        return "not found"
    pattern = re.compile(rf"^{re.escape(target_name)}\s*:")
    return "found" if any(pattern.match(line) for line in read_text(makefile).splitlines()) else "not found"


def print_summary(result: StartProjectResult) -> None:
    print("Project initialization summary")
    print_path_section("Changed files", result.changed_files)
    print_path_section("Unchanged files", result.unchanged_files)
    print_path_section("Skipped files", result.skipped_files)
    print_path_section("Protected files selected by --force", result.protected_force_files)
    print_text_section("Warnings", result.warnings)
    print_text_section("Commands run", result.commands_run)
    print_text_section("Commands skipped", result.commands_skipped)
    print_text_section("Next steps", result.next_steps)
    print_text_section("Failures", result.failed)
    if result.dry_run_diffs:
        print("\nPlanned changes:")
        for diff in result.dry_run_diffs:
            print(diff.rstrip())


def print_path_section(title: str, values: Sequence[Path]) -> None:
    print(f"\n{title}:")
    if not values:
        print("- none")
        return
    for value in values:
        print(f"- {value}")


def print_text_section(title: str, values: Sequence[str]) -> None:
    print(f"\n{title}:")
    if not values:
        print("- none")
        return
    for value in values:
        print(f"- {value}")


def run_cli(
    argv: Sequence[str],
    *,
    project_root: Path | None = None,
    input_func: Callable[[str], str] = input,
    runner_func: CommandRunner | None = None,
    which_func: WhichFunc = shutil.which,
    emit_summary: bool = True,
) -> StartProjectResult:
    args = parse_args(argv)
    root = project_root or Path.cwd()
    if args.non_interactive and not args.answers:
        result = StartProjectResult(exit_code=2)
        result.failed.append("--non-interactive requires --answers.")
        if emit_summary:
            print_summary(result)
        return result

    try:
        startup_warnings: list[str] = []
        if args.answers:
            answers = load_answers_file(Path(args.answers))
        else:
            answers, startup_warnings = load_resume_answers(root)
        if not args.non_interactive:
            if emit_summary:
                print_preflight_summary(root, answers, which_func)
            answers = collect_interactive_answers(input_func=input_func, existing_answers=answers)
        else:
            answers = normalize_answers(answers)
    except (OSError, ValueError, json.JSONDecodeError) as error:
        result = StartProjectResult(exit_code=2)
        result.failed.append(str(error))
        if emit_summary:
            print_summary(result)
        return result

    options = StartProjectOptions(
        dry_run=args.dry_run,
        force=args.force,
        skip_audit=args.skip_audit,
        skip_render=args.skip_render,
    )
    result = initialize_project(root, answers, options, runner_func=runner_func, which_func=which_func)
    result.warnings = startup_warnings + result.warnings
    if emit_summary:
        print_summary(result)
    return result


def generated_html_comment() -> str:
    return f"<!-- {GENERATED_MARKER}; this file records setup decisions, not evidence. -->"


def project_purpose(answers: Mapping[str, object]) -> str:
    return (
        f"Initialize a {answers['project_type']} project around the central question without drafting unsupported "
        "claims before sources, notes, and evidence checks exist."
    )


def source_base_text(answers: Mapping[str, object]) -> str:
    return f"Use Zotero + Better BibTeX or `{answers['bibliography_path']}` as the verified bibliography source of truth."


def citation_readiness_notes(answers: Mapping[str, object]) -> list[str]:
    better_bibtex_status = string_value(answers["better_bibtex_auto_export"])
    notes = [
        f"- Better BibTeX auto-export: {better_bibtex_status}",
        "- Zotero plus Better BibTeX is expected unless this project intentionally maintains the BibTeX file by hand.",
    ]
    if better_bibtex_status.lower() == "yes":
        notes.append("- Verify the Better BibTeX export path before adding manuscript citations.")
    else:
        notes.append("- Do not rely on automatic bibliography updates until Better BibTeX export is configured and checked.")
    return notes


def unresolved_marker(answers: Mapping[str, object]) -> str:
    return STRICT_UNRESOLVED_TEXT if should_run_step(answers.get("strict_placeholder_detection")) else UNRESOLVED_TEXT


def display_scalar(answers: Mapping[str, object], key: str) -> str:
    value = string_value(answers[key])
    return unresolved_marker(answers) if is_unresolved(value) else value


def first_or_unresolved(value: object, marker: str = UNRESOLVED_TEXT) -> str:
    values = ensure_string_list(value)
    return values[0] if values else marker


def bullet_list_or_unresolved(value: object, marker: str = UNRESOLVED_TEXT) -> str:
    values = ensure_string_list(value)
    if not values:
        return f"- {marker}"
    return "\n".join(f"- {item}" for item in values)


def inline_list_or_unresolved(value: object, marker: str = UNRESOLVED_TEXT) -> str:
    values = ensure_string_list(value)
    return ", ".join(values) if values else marker


def optional_or_unresolved(value: object, marker: str = UNRESOLVED_TEXT) -> str:
    text = string_value(value).strip()
    return text if text else marker


def is_unresolved(value: object) -> bool:
    text = string_value(value).strip()
    return text.lower() in UNKNOWN_VALUES or text.startswith("Unresolved:")


def main(argv: Sequence[str]) -> int:
    change_to_project_root()
    result = run_cli(argv, project_root=Path.cwd())
    return result.exit_code


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
