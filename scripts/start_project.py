def default_chapter_names(project_type: str) -> list[str]:
    normalized = normalize_project_type(project_type)
    if normalized == "article":
        return ["Introduction", "Literature review", "Research approach", "Analysis", "Conclusion"]
    if normalized == "thesis-style manuscript":
        return ["Introduction", "Literature review", "Methodology", "Findings", "Discussion", "Conclusion"]
    if normalized == "mixed paper/book project":
        return ["Introduction", "Literature map", "Core argument", "Case or evidence chapter", "Conclusion"]
    return ["Introduction", "Literature map", "Evidence base", "Argument development", "Conclusion"]


def collect_interactive_answers(
    *,
    input_func: Callable[[str], str] = input,
    existing_answers: Mapping[str, object] | None = None,
) -> dict[str, object]:
    answers = dict(existing_answers or {})
    for question in QUESTIONS:
        current = answers.get(question.key)
        if current not in (None, "", []):
            continue
        default_text = f" [{question.default}]" if question.default else ""
        prompt = f"{question.prompt}{default_text} (enter unknown/decide later if unresolved): "
        raw_value = input_func(prompt).strip()
        if raw_value == "" and question.default:
            raw_value = question.default
        answers[question.key] = split_list(raw_value) if question.multi else raw_value
    return normalize_answers(answers)


def load_answers_file(path: Path) -> dict[str, object]:
    text = read_text(path)
    if path.suffix.lower() == ".json":
        loaded = json.loads(text)
        if not isinstance(loaded, dict):
            raise ValueError("Answers JSON must contain an object at the top level.")
        return dict(loaded)
    if path.suffix.lower() in {".yml", ".yaml"}:
        return parse_simple_yaml(text)
    raise ValueError(f"Unsupported answers file extension: {path.suffix}. Use .json, .yml, or .yaml.")


def load_resume_answers(project_root: Path) -> tuple[dict[str, object], list[str]]:
    answers_path = project_root / PROJECT_START_PATH
    if not answers_path.exists():
        return {}, []
    return load_answers_file(answers_path), [f"Resumed answers from {PROJECT_START_PATH}."]


def parse_simple_yaml(text: str) -> dict[str, object]:
    answers: dict[str, object] = {}
    current_list_key: str | None = None
    for raw_line in text.splitlines():
        line = raw_line.rstrip()
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        if line.startswith("  - ") and current_list_key:
            answers.setdefault(current_list_key, [])
            list_value = answers[current_list_key]
            if not isinstance(list_value, list):
                raise ValueError(f"YAML key {current_list_key!r} mixes scalar and list values.")
            list_value.append(parse_yaml_scalar(line[4:].strip()))
            continue
        current_list_key = None
        if ":" not in line:
            raise ValueError(f"Unsupported YAML line: {line!r}")
        key, raw_value = line.split(":", 1)
        key = key.strip()
        if key not in ANSWER_KEYS:
            continue
        value = raw_value.strip()
        if value == "":
            if key in LIST_KEYS:
                answers[key] = []
                current_list_key = key
            else:
                answers[key] = ""
        else:
            answers[key] = parse_yaml_scalar(value)
    return answers


def parse_yaml_scalar(value: str) -> str:
    if len(value) >= 2 and value[0] == value[-1] and value[0] in {'"', "'"}:
        if value[0] == '"':
            return str(json.loads(value))
        return value[1:-1].replace("''", "'")
    return value


def dump_answers_yaml(answers: Mapping[str, object]) -> str:
    lines = [
        f"# {GENERATED_MARKER}",
        "# This file records project-start answers; it is not evidence or bibliography metadata.",
    ]
    for key in ANSWER_KEYS:
        value = answers.get(key, "")
        if key in LIST_KEYS:
            values = ensure_string_list(value)
            lines.append(f"{key}:")
            if values:
                lines.extend(f"  - {format_yaml_scalar(item)}" for item in values)
            continue
        lines.append(f"{key}: {format_yaml_scalar(string_value(value))}")
    lines.append("")
    return "\n".join(lines)


def format_yaml_scalar(value: str) -> str:
    if value == "":
        return '""'
    unsafe = re.search(r"[:#\[\]{}&*!|>'\"%@`]", value) or value.strip() != value
    yaml_keyword = value.lower() in {"true", "false", "yes", "no", "null", "~"}
    if unsafe or yaml_keyword:
        return json.dumps(value)
    return value


def normalize_answers(raw_answers: Mapping[str, object]) -> dict[str, object]:
    normalized: dict[str, object] = {}
    for question in QUESTIONS:
        raw_value = raw_answers.get(question.key, question.default)
        if question.multi:
            normalized[question.key] = normalize_list(raw_value)
            continue
        normalized[question.key] = normalize_scalar(raw_value, question.default)

    normalized["working_title"] = normalize_required_text(normalized["working_title"], "Untitled project")
    normalized["author_name"] = unresolved_text(normalized["author_name"])
    normalized["project_type"] = normalize_project_type(string_value(normalized["project_type"]))
    normalized["project_slug"] = normalize_project_slug(
        string_value(normalized["project_slug"]),
        string_value(normalized["working_title"]),
    )
    normalized["central_research_question"] = unresolved_text(normalized["central_research_question"])
    normalized["working_thesis"] = normalize_required_text(normalized["working_thesis"], "unknown yet")
    normalized["primary_audience"] = unresolved_text(normalized["primary_audience"])
    normalized["secondary_audience"] = unresolved_text(normalized["secondary_audience"], empty_allowed=True)
    normalized["output_formats"] = normalize_output_formats(normalized["output_formats"])
    if not normalized["chapter_names"]:
        normalized["chapter_names"] = default_chapter_names(string_value(normalized["project_type"]))
    normalized["citation_style"] = normalize_required_text(normalized["citation_style"], "undecided")
    normalized["target_venue"] = normalize_required_text(normalized["target_venue"], "undecided")
    normalized["bibliography_path"] = normalize_required_text(
        normalized["bibliography_path"],
        DEFAULT_BIBLIOGRAPHY_PATH.as_posix(),
    )
    return normalized


def normalize_project_type(value: str) -> str:
    lowered = value.strip().lower()
    if lowered in {"thesis", "thesis-style", "thesis style", "thesis-style manuscript"}:
        return "thesis-style manuscript"
    if lowered in {"mixed", "mixed paper/book", "paper/book", "mixed paper/book project"}:
        return "mixed paper/book project"
    if lowered in PROJECT_TYPES:
        return lowered
    if lowered in UNKNOWN_VALUES:
        return "book"
    return lowered


def normalize_required_text(value: object, fallback: str) -> str:
    text = string_value(value).strip()
    return fallback if text.lower() in UNKNOWN_VALUES else text


def normalize_scalar(value: object, default: str) -> str:
    text = string_value(value).strip()
    if text == "" and default:
        return default
    return text


def unresolved_text(value: object, *, empty_allowed: bool = False) -> str:
    text = string_value(value).strip()
    if empty_allowed and text == "":
        return ""
    return UNRESOLVED_TEXT if text.lower() in UNKNOWN_VALUES else text


def normalize_list(value: object) -> list[str]:
    return [item for item in ensure_string_list(value) if item.strip().lower() not in UNKNOWN_VALUES]


def normalize_output_formats(value: object) -> list[str]:
    values = [item.lower() for item in normalize_list(value)]
    if not values:
        return ["html"]
    unique_values: list[str] = []
    for item in values:
        normalized = item.strip().lower()
        if normalized == "word":
            normalized = "docx"
        if normalized not in unique_values:
            unique_values.append(normalized)
    return unique_values


def split_list(value: str) -> list[str]:
    return [item.strip() for item in re.split(r"[,;\n]", value) if item.strip()]


def ensure_string_list(value: object) -> list[str]:
    if isinstance(value, list):
        return [string_value(item).strip() for item in value if string_value(item).strip()]
    if isinstance(value, tuple):
        return [string_value(item).strip() for item in value if string_value(item).strip()]
    return split_list(string_value(value))


def string_value(value: object) -> str:
    if value is None:
        return ""
    if isinstance(value, str):
        return value
    return str(value)


def normalize_project_slug(raw_slug: str, title: str) -> str:
    slug = slugify(raw_slug or title or "research-project")
    if not slug:
        slug = "research-project"
    return avoid_forbidden_codex_prefix(slug, "project")


def slugify(value: str) -> str:
    lowered = value.strip().lower()
    replaced = re.sub(r"[^a-z0-9]+", "-", lowered)
    return replaced.strip("-")


def avoid_forbidden_codex_prefix(slug: str, replacement_prefix: str) -> str:
    if slug.lower().startswith("codex"):
        return f"{replacement_prefix}-{slug}"
    return slug


def build_planned_changes(project_root: Path, answers: Mapping[str, object], result: StartProjectResult) -> list[PlannedChange]:
    changes = [
        PlannedChange(project_root / PROJECT_START_PATH, dump_answers_yaml(answers)),
        PlannedChange(project_root / QUARTO_CONFIG_PATH, render_quarto_config(project_root, answers)),
        PlannedChange(project_root / MANUSCRIPT_INDEX_PATH, render_manuscript_index(answers)),
        PlannedChange(project_root / FRONT_MATTER_PATH, render_front_matter(answers)),
        PlannedChange(project_root / BACK_MATTER_PATH, render_back_matter(answers)),
    ]
    charter = render_project_charter(project_root, answers, result)
    if charter is not None:
        changes.append(PlannedChange(project_root / PROJECT_CHARTER_PATH, charter))
    for relative_path, content in render_chapter_files(answers).items():
        changes.append(PlannedChange(project_root / relative_path, content))
    bibliography_change = render_bibliography_placeholder(project_root, answers)
    if bibliography_change is not None:
        changes.append(PlannedChange(bibliography_change[0], bibliography_change[1]))
    return changes


def render_project_charter(
    project_root: Path,
    answers: Mapping[str, object],
    result: StartProjectResult,
) -> str | None:
    template_path = project_root / PROJECT_CHARTER_TEMPLATE_PATH
    if not template_path.exists():
        result.warnings.append(f"Missing {PROJECT_CHARTER_TEMPLATE_PATH}; skipped project charter.")
        return None
    template = read_text(template_path)
    unresolved = unresolved_marker(answers)
    replacements = {
        "TITLE": string_value(answers["working_title"]),
        "PURPOSE": project_purpose(answers),
        "PRIMARY_READERS": display_scalar(answers, "primary_audience"),
        "QUESTION": display_scalar(answers, "central_research_question"),
        "IN_SCOPE_BOUNDARY": first_or_unresolved(answers["scope_included"], unresolved),
        "OUT_OF_SCOPE_BOUNDARY": first_or_unresolved(answers["scope_excluded"], unresolved),
        "KNOWN_SOURCE_BASE": source_base_text(answers),
        "KEY_UNCERTAINTY": first_or_unresolved(answers["main_uncertainties"], unresolved),
        "FIRST_RESEARCH_TASK": first_or_unresolved(answers["initial_research_tasks"], unresolved),
    }
    body = replace_placeholders(template, replacements).rstrip()
    body = append_list_items(body, "### In scope", answers["scope_included"])
    body = append_list_items(body, "### Out of scope", answers["scope_excluded"])
    body = append_list_items(body, "## Key uncertainties", answers["main_uncertainties"])
    body = append_list_items(body, "## First research tasks", answers["initial_research_tasks"])
    return "\n\n".join([generated_html_comment(), body, render_charter_extension(answers)]).rstrip() + "\n"


def append_list_items(text: str, heading: str, values: object) -> str:
    items = ensure_string_list(values)
    if len(items) <= 1 or heading not in text:
        return text
    first_item = first_or_unresolved(items)
    extra_items = [item for item in items[1:] if item != first_item]
    if not extra_items:
        return text
    insertion = "\n".join(f"- {item}" for item in extra_items)
    return text.replace(f"- {first_item}", f"- {first_item}\n{insertion}", 1)


