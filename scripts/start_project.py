def render_quarto_config(project_root: Path, answers: Mapping[str, object]) -> str:
    chapter_paths = ["index.qmd", "chapters/00-front-matter.qmd"]
    chapter_paths.extend(path.as_posix() for path in chapter_quarto_paths(answers))
    chapter_paths.append("chapters/99-back-matter.qmd")

    lines = [
        f"# {GENERATED_MARKER}",
        "project:",
        "  type: book",
        "  output-dir: ../exports/html",
        "",
        "book:",
        f"  title: {quote_quarto_value(answers['working_title'])}",
    ]
    if not is_unresolved(answers["subtitle"]):
        lines.append(f"  subtitle: {quote_quarto_value(answers['subtitle'])}")
    if not is_unresolved(answers["author_name"]):
        lines.append(f"  author: {quote_quarto_value(answers['author_name'])}")
    lines.append("  chapters:")
    lines.extend(f"    - {chapter_path}" for chapter_path in chapter_paths)
    lines.extend(
        [
            "",
            f"bibliography: ../{answers['bibliography_path']}",
            "",
            "format:",
        ]
    )
    quarto_formats = [item for item in ensure_string_list(answers["output_formats"]) if item in KNOWN_QUARTO_FORMATS]
    if not quarto_formats:
        quarto_formats = ["html"]
    for output_format in quarto_formats:
        lines.extend([f"  {output_format}:", "    toc: true"])
    lines.append("")
    return "\n".join(lines)


def quote_quarto_value(value: object) -> str:
    return json.dumps(string_value(value))


